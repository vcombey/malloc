#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	while (i < len)
	{
		dst[i] = src[i];
		if (src[i] == '\0')
		{
			i++;
			while (i < len)
				dst[i++] = '\0';
			break ;
		}
		i++;
	}
	return (dst);
}
